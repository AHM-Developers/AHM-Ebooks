function showResults(searchTerm) {
  const searchResults = document.getElementById("searchResults");
  const matchingBooks = allbooks.filter(book => {
    return book.title.toLowerCase().includes(searchTerm.toLowerCase());
  });

  // If there are no matching books, hide the search results container
  if (matchingBooks.length === 0 || searchTerm === "") {
    searchResults.style.display = "none";
    searchResults.innerHTML = ""; // Clear previous results
    return;
  }

  // Clear previous results and display the search results container
  searchResults.style.display = "block";
  searchResults.innerHTML = ""; // Clear previous results
  
  // Display matching books
  matchingBooks.forEach(function(book) {
    const bookElement = document.createElement("a");
    bookElement.href = book.link;
    bookElement.textContent = book.title;
    bookElement.classList.add("book-link");

    // Add icon
    const icon = document.createElement("span");
    icon.classList.add("icon");
    icon.textContent = "📘"; // You can replace this with any blue-colored book icon
    bookElement.insertBefore(icon, bookElement.firstChild);

    // Add event listener for mouseover to populate search bar on hover
    bookElement.addEventListener("mouseover", function() {
      document.getElementById("searchInput").value = book.title;
    });

    // Add event listener for mouseout to clear search bar when mouse leaves
    bookElement.addEventListener("mouseout", function() {
      document.getElementById("searchInput").value = ""; // Clear search bar value
    });

    searchResults.appendChild(bookElement);
  });
}


// Event listener for keyup event on search input
document.getElementById("searchInput").addEventListener("keyup", function(event) {
  const searchTerm = event.target.value.trim(); // Get the search term and trim whitespace
  showResults(searchTerm); // Call the function to display search results
});








