    // Function to start speech recognition and listen for voice commands
    function startVoiceCommand() {
      // Check if browser supports SpeechRecognition
      if ('SpeechRecognition' in window || 'webkitSpeechRecognition' in window) {
          // Create new instance of SpeechRecognition
          var recognition = new (window.SpeechRecognition || window.webkitSpeechRecognition)();

          // Set continuous to true to allow continuous speech recognition
          recognition.continuous = true;

          // Start recognition
          recognition.start();

          // Add event listener for result event
          recognition.addEventListener('result', function(event) {
              // Iterate through recognized speech
              for (var i = event.resultIndex; i < event.results.length; i++) {
                  // Get transcript of recognized speech
                  var transcript = event.results[i][0].transcript.toLowerCase();
                  
                  // Check if transcript contains "show all"  ab jo user ne bola h ham usko transcript mn  save kr rhe hn or usse condtio de rhe hn ke agar user ne "show all" bola h to showCards()funtion hlao  
                  if (transcript.includes('show all books')) {
                      // If the command is recognized, call the showCards function
                      showall();
                  }
                  else if(transcript.includes('hello'))
                  {
                    alert("hello! Dear user, How are you?");
                  }
                  else if(transcript.includes('hide all books'))
                  {
                    hideallcards();
                  }
                  else if(transcript.includes('search books'))
                  {
                    handlesearching();
                  }
                  else if(transcript.includes('show story books'))
                  {
                    handlestorybooks();
                  }
                  else if (transcript.includes('show historical books')) 
                  {
                    handlehistorybooks(); // Call the function to handle "reload" command
                }
                else if(transcript.includes('reload'))
                {
                  handleReload();
                }
                else if(transcript.includes('tell me total books'))
                {
                  alert(allbooks.length);
                }
                else
                {
                  alert("say valid commands");
                }
              }
              //  } 
              //  آپ کی کوڈ میں، آپ ہر ایک نتیجے کو علیحدہ طور پر ہینڈل کرنے کے لئے انہیں علیحدہ کرنے کے لئے یہ لوپ کارروائی کر رہے ہیں۔ اس لوپ کے ذریعے آپ ہر ایک شناخت شدہ چیز (یا نتیجہ) کو علیحدہ طور پر دستیاب اور عملی بنا سکتے ہیں۔
                  // event.result ek like a array h jis mn collection h browser ke pass jo jo hm bolte hn predefined
          });

          // Add event listener for error event
          recognition.addEventListener('error', function(event) {
              console.error('Speech recognition error:', event.error);
          });
      } else {
          // Browser does not support SpeechRecognition
          console.error('Speech recognition is not supported in this browser.');
      }
  }

